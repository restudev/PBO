public class Matematika {

    void pertambahan(int a, int b) {
        int hasil = a + b;
        System.out.println("Hasil Pertambahan: " + a + " + " + b + " = " + hasil);
    }

    void pengurangan(int a, int b) {
        int hasil = a - b;
        System.out.println("Hasil Pengurangan: " + a + " - " + b + " = " + hasil);
    }

    void perkalian(int a, int b) {
        int hasil = a * b;
        System.out.println("Hasil Perkalian: " + a + " x " + b + " = " + hasil);
    }

    void pembagian(int a, int b) {
        int hasil = a / b;
        System.out.println("Hasil Pembagian: " + a + " : " + b + " = " + hasil);
    }

    void pertambahan(double a, double b, double c) {
        double hasil = a + b + c;
        System.out.println("Hasil Pertambahan: " + a + " + " + b + " + " + c + " = " + hasil);
    }

    void pengurangan(double a, double b, double c) {
        double hasil = a - b - c;
        System.out.println("Hasil Pengurangan: " + a + " - " + b + " - " + c + " = " + hasil);
    }

    void perkalian(double a, double b, double c) {
        double hasil = a * b * c;
        System.out.println("Hasil Perkalian: " + a + " x " + b + " x " + c + " = " + hasil);
    }

    void pembagian(double a, double b, double c) {
        double hasil = a / b / c;
        System.out.println("Hasil Pembagian: " + a + " : " + b + " : " + c + " = " + hasil);
    }
}

public class Matematika {
    float pertambahan(float a, float b){
        return a + b;
    }
    float pengurangan(float a, float b){
        return a - b;
    } 
    float perkalian(float a, float b){
        return a * b;
    } 
    float pembagian(float a, float b){
        return a / b;
    }     
}

public class Kubus{
public static void main(String args[])
{
	int s = 15 ;
	int vol = s*s*s;
	int luas = 6*s*s;
	int kel = 12*s;
	System.out.println("Sisi			: " +s +" cm");
	System.out.println("Volume Kubus		: " +vol + " cm^3");
	System.out.println("Luas Permukaan Kubus	: " +luas +" cm^2");
	System.out.println("Keliling Kubus		: " +kel +" cm");
}
}
